package Library;

/**
 *
 * @author ASUS
 */
 class Student {
    private String name;
    private String npm;

    // Constructor
    public Student(String name, String npm) {
        this.name = name;
        this.npm = npm;
    }

    // Getter dan Setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNpm() {
        return npm;
    }

    public void setNpm(String npm) {
        this.npm = npm;
    }
}
